package Case_Study;


public class Student extends Person{
	
	//Student
		private int numofMod;
		private int numofRepMod;
		private double amountpaid;
		private double balance;
		
		public int getNumofMod() {
			return numofMod;
		}
		public void setNumofMod(int numofMod) {
			this.numofMod = numofMod;
		}
		public int getNumofRepMod() {
			return numofRepMod;
		}
		public void setNumofRepMod(int numofRepMod) {
			this.numofRepMod = numofRepMod;
		}
		public double getAmountpaid() {
			return amountpaid;
		}
		public void setAmountpaid(double amountpaid) {
			this.amountpaid = amountpaid;
		}
		public double getBalance() {
			return balance;
		}
		public void setBalance(double balance) {
			this.balance = balance;
		}
	
		   
}
